<?php
/**
 * index.php — Точка входа и bootstrap приложения Nirva.
 *
 * Находится в корне public_html/ — именно этот файл запускает веб-сервер
 * при каждом HTTP-запросе к приложению.
 * Его единственная задача — последовательно инициализировать все
 * необходимые компоненты и подключить нужный view (страницу).
 *
 * Порядок инициализации важен:
 *   1. session_start()              → PHP-сессия должна стартовать до любого вывода
 *   2. config/app.php               → настройки окружения и логирования
 *   3. config/database.php          → константы подключения к БД
 *   4. src/services/Database/Database.php → класс для работы с БД (использует константы из п.3)
 *   5. pages/landing/landing.php    → HTML-страница, которую увидит пользователь
 *
 * Запрещено добавлять в этот файл:
 *   - HTML-разметку
 *   - бизнес-логику
 *   - SQL-запросы
 */

// ─── 1. Сессия ────────────────────────────────────────────────────────────────

/**
 * session_start() — инициализирует PHP-сессию.
 *
 * Создаёт или восстанавливает сессию пользователя по cookie PHPSESSID.
 * После вызова суперглобальный массив $_SESSION доступен во всех
 * подключённых файлах: landing.php и любых будущих страницах.
 *
 * Должна вызываться ДО любого вывода (echo, HTML) — иначе PHP выдаст
 * ошибку "Cannot send session cookie - headers already sent".
 */
session_start();

// ─── 2. Конфигурация приложения ───────────────────────────────────────────────

/**
 * require_once — подключает файл ровно один раз; при повторном вызове игнорируется.
 * Если файл не найден — фатальная ошибка (в отличие от include, который лишь предупреждает).
 *
 * __DIR__ — абсолютный путь к папке, в которой находится этот файл (корень public_html/).
 * Используем __DIR__ вместо относительных путей '../', чтобы пути работали корректно
 * независимо от того, из какой директории запускается PHP.
 *
 * app.php задаёт:
 *   - APP_ENV, APP_DEBUG
 *   - error_reporting, display_errors
 *   - путь к лог-файлу /logs/php-errors.log
 */
require_once __DIR__ . '/config/app.php';

// ─── 3. Конфигурация базы данных ──────────────────────────────────────────────

/**
 * database.php задаёт константы DB_HOST, DB_NAME, DB_USER, DB_PASSWORD.
 * Подключается после app.php, чтобы логирование уже было настроено
 * на случай ошибки в самом файле конфигурации.
 */
require_once __DIR__ . '/config/database.php';

// ─── 4. Сервис базы данных ────────────────────────────────────────────────────

/**
 * Database.php определяет класс Database с методом Database::getConnection().
 * Подключается после database.php, так как использует его константы
 * (DB_HOST, DB_NAME, DB_USER, DB_PASSWORD) внутри PDO-конструктора.
 *
 * Соединение с БД не создаётся здесь — оно создаётся лениво (lazy),
 * только когда какой-либо компонент впервые вызовет Database::getConnection().
 */
require_once __DIR__ . '/src/services/Database/Database.php';

// ─── 5. Отображение страницы ──────────────────────────────────────────────────

/**
 * include (не require_once) — подключает landing.php как шаблон страницы.
 *
 * Используем include, а не require_once, потому что:
 *   - В будущем здесь появится роутер, который будет выбирать нужный шаблон.
 *   - include позволяет подключать разные файлы в зависимости от маршрута.
 *
 * landing.php содержит весь HTML текущей страницы и является единственным
 * местом в этом запросе, где генерируется вывод для браузера.
 */
include __DIR__ . '/pages/landing/landing.php';
