<?php
/**
 * config/app.php — Глобальная конфигурация приложения Nirva.
 *
 * Подключается первым в pages/index.php через require_once.
 * Определяет окружение, режим отладки и настройки логирования ошибок PHP.
 * Все остальные файлы проекта наследуют эти настройки автоматически,
 * так как PHP применяет ini_set() глобально на время запроса.
 */

// ─── Окружение ────────────────────────────────────────────────────────────────

/**
 * APP_ENV — текущее окружение приложения.
 * Возможные значения: 'development', 'production'.
 * Используется в будущем для условного поведения (кеш, логи, отладка).
 */
define('APP_ENV', 'development');

/**
 * APP_DEBUG — флаг режима отладки.
 * true  → показывать ошибки на экране (удобно при разработке).
 * false → скрывать ошибки от пользователя (обязательно на продакшене).
 */
define('APP_DEBUG', true);

// ─── Отображение ошибок ───────────────────────────────────────────────────────

if (APP_DEBUG) {
    /**
     * E_ALL — сообщать обо всех типах ошибок PHP:
     * notices, warnings, deprecations, fatal errors и т.д.
     * Активируется только в режиме отладки.
     */
    error_reporting(E_ALL);

    /**
     * display_errors = 1 → PHP выводит ошибки прямо в браузер.
     * Полезно при разработке, опасно на продакшене (утечка информации).
     */
    ini_set('display_errors', 1);
} else {
    /**
     * На продакшене ошибки полностью скрыты от браузера.
     * error_reporting(0) — не генерировать сообщения об ошибках.
     * display_errors = 0 — не выводить их в ответ.
     */
    error_reporting(0);
    ini_set('display_errors', 0);
}

// ─── Логирование ошибок ───────────────────────────────────────────────────────

/**
 * log_errors = 1 → PHP записывает все ошибки в лог-файл.
 * Работает в обоих режимах (development и production).
 */
ini_set('log_errors', 1);

/**
 * Путь к файлу логов ошибок PHP.
 *
 * dirname(__DIR__) — поднимается на один уровень выше текущей папки config/.
 * Результат: /public_html/logs/php-errors.log
 *
 * Папка logs/ создана в корне проекта и содержит .gitkeep,
 * чтобы она попала в git-репозиторий даже без файлов.
 */
ini_set('error_log', dirname(__DIR__) . '/logs/php-errors.log');
